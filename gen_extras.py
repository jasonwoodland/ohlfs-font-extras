def format_row(row8: str, y: int) -> str:
    """Convert an 8-char (cols 0-7) bitmap row to the canonical 22-char
    11-col format (cols -1..9), with each pixel doubled to 2 chars."""
    assert len(row8) == 8, f"row must be 8 chars: {row8!r}"
    cells = ["."] + list(row8) + [".", "."]            # cols -1, 0..7, 8, 9
    doubled = "".join("##" if c == "#" else ".." for c in cells)
    suffix = ROW_LABELS.get(y, "")
    return f"  {doubled}{suffix}"


def emit() -> None:
    out: list[str] = []
    out.append("# Ohlfs glyph extras — arrows, geometric shapes, block elements.")
    out.append("# Same format as glyphs.txt: 22-char rows × 16 rows, '##' = on, '..' = off.")
    out.append("# These are designed for adv=800 (8-px cell) and use cols 0..7.")
    out.append("")

    for name, cp, rows8 in GLYPHS:
        out.append(f"glyph {name} U+{cp:04X}")
        # Always lead with empty y=12 row.
        out.append(format_row("........", 12))
        # Then 15 rows from y=11 down to y=-3.
        for i, row8 in enumerate(rows8):
            y = 11 - i
            out.append(format_row(row8, y))
        out.append("")

    OUT.write_text("\n".join(out))
    print(f"Wrote {OUT} ({len(GLYPHS)} glyphs, {OUT.stat().st_size} bytes)")


if __name__ == "__main__":
    emit()
